// Switch Statement
let lightBulbStatus = "On";

//Start coding here
switch (lightBulbStatus) {
    case "On":
        console.log("Lightbulb is On.");
        break;
    case "Broken":
        console.log("Light bulb is Broken.");
        break;
    case "Off":
        console.log("Lightbulb is Off.");
        break;
    default:
        console.log("Please choose the correct input (On/Off/Broken)");
}
